#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char	*result;
	size_t	new_len;

	if (ft_strlen(s) < start)
		return (ft_strdup(""));
	new_len = ft_strlen(s + start);
	if (new_len < len)
		len = new_len;
	result = malloc(len + 1);
	if (!result)
		return (0);
	ft_strlcpy(result, s + start, len + 1);
	return (result);
}
