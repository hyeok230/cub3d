#include "libft.h"

char	*ft_strdup(const char *src)
{
	int		size;
	int		i;
	char	*str;

	i = 0;
	size = ft_strlen((char *)src);
	str = malloc(size + 1);
	if (!str)
		return (0);
	while (src[i])
	{
		str[i] = src[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
